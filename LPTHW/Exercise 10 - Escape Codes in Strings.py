tabby_cat = "\tI'm tabbed in."  # This creates a tab
persian_cat = "I'm split\non a line."  # \n will start a new line
backslash_cat = "I'm \\ a \\ cat."  # \\ will create a backslash

# This creates a string across multiple lines and we are using \t to cretate a tabbed line
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t*Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)


# \\ Backslash(\)
# \' Single-quote (')
# \" Double-quote (")
# \n new-line
# \t Horizontal tab
# \ ignore the next character
